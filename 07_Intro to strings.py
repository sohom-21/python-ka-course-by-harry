
# String is a data type in python
# string is a sequence of characters enclosed in quotes

#we can write strings in python in 3 ways
name = 'Sohom' #single quoted strings
name1  = "Sohom" #double quoted strings
name2 = '''Sohom''' #triple quoted strings

print("This the string written single quotes",name)
print("This the string written double quotes",name1)
print("This the string written triple quotes",name2)